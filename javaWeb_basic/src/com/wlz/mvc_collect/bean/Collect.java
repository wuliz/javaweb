package com.wlz.mvc_collect.bean;

public class Collect {
private int id;
private String name;
private String url;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public String getUrl() {
	return url;
}
public void setUri(String uri) {
	this.url = uri;
}
}
